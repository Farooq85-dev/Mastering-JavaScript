// String Data Type:-
// String represents textual data.

// In JavaScript we can define string via three methods.

const strWithSingleQuotes = 'Hello World!';
console.log(typeof strWithSingleQuotes); // string

const strWithDoubleQuotes = "Hello World!";
console.log(typeof strWithDoubleQuotes); // string

const strWithBackitcs = `Hello World!`;
console.log(typeof strWithBackitcs); // string

// Escaping character with double quotes
const nestedDoubleQuotes = "My name is Muhammad Farooq. I doesn\"t like carrots.";
console.log(nestedDoubleQuotes);

// Escaping character with single quotes
const nestedSingleQuotes = 'Hello Man\'s!'
console.log(nestedSingleQuotes);
