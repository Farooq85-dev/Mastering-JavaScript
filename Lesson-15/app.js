// Data Types:-

// String
const userName = "M. Farooq";
console.log(typeof userName); // string

// Number
const userId = 102;
console.log(typeof userId); // number

// Boolean
const isUserVerified = true;
console.log(typeof isUserVerified); // boolean

// Array
const arrayOfNumbers = [1, 23, 3, 3];
console.log(typeof arrayOfNumbers); // object

// Object
const students = {
  userName: "Farooq",
  fatherName: "Irshad",
};

console.log(typeof students); // object

// null
const isNull = null;
console.log(typeof isNull); // object

// undefined
const isUndefined = undefined;
console.log(typeof isUndefined); // undefined

// Date
const now = new Date();
console.log(typeof now); // object

