// Ternory Operator:-

// Ternory means composed of 3 parts.
// Ternory is an alternative to if-else.

let success = true;
if (success) {
  console.log("True!");
} else {
  console.log("False!");
}

// Using Ternory operator

// Basic syntax
// <condition> ? True Case : False Case
success ? "True!" : "False";
